package containers;

/**
 * Flat container class for heroes
 * 
 * @author gitterrost4
 */
public class Hero {

  public final String name;
  public final String emoji;
  public final String skill1;

  public Hero(String name, String emoji, String skill1) {
    super();
    this.name = name;
    this.emoji = emoji;
    this.skill1 = skill1;
  }

}
